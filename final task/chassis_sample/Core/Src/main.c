int16_t  encoder;
bool dir;

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
    if (GPIO_Pin == ENCODER_Pin) {
    	if (dir == 1){
    		encoder++;
		}else{
			encoder--;
		}
    }
}


#define MAX_SPEED_PWM 30000
#define ABS(x) ( (x) > 0 ? (x) : -(x) )
void motorSetVel(float vel){ // vel \in [-1.0; 1.0]
	int16_t Speed = MAX_SPEED_PWM*ABS(vel);

	if (vel > 0){
		dir = 1;

		TIM22 -> CCR1 = 0;
	    TIM22 -> CCR2 = Speed;
	}else{
		dir = 0;

		TIM22 -> CCR1 = Speed;
		TIM22 -> CCR2 = 0;
	}
}

bool motorGoToPosition(int16_t position){
	bool ret = 0;

	if (position == encoder){
		motorSetVel(0.0);

		ret = 1;
	}else{

		if (position > encoder){
			motorSetVel(0.5);
		}else{
			motorSetVel(-0.5);
		}

		ret = 0;
	}

	return ret;
}

